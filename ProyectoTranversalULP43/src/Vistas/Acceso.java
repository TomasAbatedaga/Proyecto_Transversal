    private void jbtAceptoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAceptoActionPerformed
        // TODO add your handling code here:
        String elUser = jtUsuario.getText();
        char[] elPass = jPClave.getPassword();
        if (!elUser.isEmpty() && elPass.length > 0) {
            String usuario = jtUsuario.getText();
            char[] cl = jPClave.getPassword();
            if(acceso.validarAcceso(usuario, cl)){
                this.setVisible(false);
                MenuPrincipal.main(null);
            }else{
                JOptionPane.showMessageDialog(null, "El usuario o la clave no son vÃ¡lidas");
            }
        }
    }//GEN-LAST:event_jbtAceptoActionPerformed

    private void jPClaveKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPClaveKeyReleased
        if (jPClave.getPassword().length > 0) {
            jbtAcepto.setEnabled(true);
        }else{
            jbtAcepto.setEnabled(false);
        }
    }//GEN-LAST:event_jPClaveKeyReleased

